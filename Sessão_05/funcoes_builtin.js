//O JavaScript possui funções integradas que podem ser usadas para realizar perações comuns.
//Estas funções são parte do objeto global. e podem ser usadas sem a necessidade de declarar ou importar nada.

//Funções Matemáticas
console.log(Math.PI);
console.log(Math.round(4.7));
console.log(Math.pow(8, 2));
console.log(Math.sqrt(64));

//Funções de Data
const date = new Date();

//Funções de data
console.log(date); //data atual
console.log(date.getFullYear()) //exibe o ano atual
console.log(date.getMonth()) //exibe o mes atual
console.log(date.getDay());

