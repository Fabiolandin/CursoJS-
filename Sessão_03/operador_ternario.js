//Operador ternario é uma forma mais simples de usar o if e else em JavaScript.
//É também conhecido como operador condicional ou operador de condição
//(comparação), se sim (resposta), se não (resposta)

let myName = 'Jhon';
console.log(myName === 'Jhon' ? 'Hellow Jhon' : 'Hellow Visitante'); //Hellow Jhon

myName = 'Jane';
console.log(myName === 'Jhon' ? 'Hellow Jhon' : 'Hellow Visitante'); //Hellow Visitante


