//Os operadores aritmeticos são usados para realizar operações matematicas em javascript
// + (adição), - (Subtração), / (Divisão), *(Multiplicação), %(Módulo), **(Exponenciação).

let num1 = 10;
let num2 = 5;

//Adição
let soma = num1 + num2;

//Subtração
let subtracao = num1 - num2;

//Divisão
let divisao = num1 / num2;

//Multiplicação
let multiplicacao = num1 * num2;

//Módulo
let modulo = num1 % num2; // 10 % 5 = 0
let modulo2 = 11 % 5;     // 11 % 5 = 1

//Exponenciação
let exponenciacao = num1 ** num2; // 10 ** 5 = 100000

//Operadores Unarios
