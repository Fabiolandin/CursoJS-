//Os operadores de comparação são usados para comparar dois valores
//Eles retornam true ou false dependendo do resultado da comparação.

let num_1 = 5;  
let num_2 = 10;

// operador de comparação (==)
console.log(num_1 == num_2);    // false

// operador de negação de comparação (!=)
console.log(num_1 != num_2);    // true

// operador de comparação maior que (>)
console.log(num_1 > num_2);    // false

// operador de comparação maior ou igual que (>=)
console.log(num_1 >= num_2);    // false

// operador de comparação menor que (<)
console.log(num_1 < num_2);    // true
