//Estruturas condicionais: if....else...

let temperatura = 20;

console.log("Vamos ver como está o tempo!");

//Instrução IF simples
if (temperatura == 20){
    console.log("Estão 20 graus!");
}

//Instrução if....else
if (temperatura <= 0){
    console.log("Está abaixo de zero!");
} else {
    console.log("Está acima de zero!");
}