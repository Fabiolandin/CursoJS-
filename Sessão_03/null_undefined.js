//Tipos de dados 'null' e 'undefined' são dois tipos de dados primitivos em javascript.
//os dois representam a ausencia de valor porém só um é recomendado

let myNull = null; //recomendado
console.log(myNull); // null

let myUndefined = undefined; //udefined pode ser atribuido a uma variavel, mas NÃO É RECOMENDADO
console.log(myUndefined)

// Undefined significa que ainda não foi atribuido um valor á variavel

let myNumber; //nenhum valor atribuido
console.log(myNumber); //undefined