//O bloco de código é um conjunto de instruções agrupadas entre chaves {}.
//Serve para organizar o código e definir o escopo

//Uma instrução if fica em um bloco de código
if (true) {
    console.log('This is true');
}

//Um loop for com um bloco de código
for (let i = 0; i < 10; i++ ) {
    console.log(i)
}

//Uma função com um bloco de código
function myFunction() {
    console.log('This is a function');
}