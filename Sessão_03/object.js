//Um dos tipos de dados mais importantes de JS é o 'object'
//objetos são estruturas de dados que podem armazenar coleções de dados e funcionalidades.

//definindo um objeto
let myObject = {
    name: 'Mike',    //propriedade nome
    age: '31',       //propriedade idade
    isMarried: false //propriedade casado ou não
};

//Apresentando o objeto completo
console.log(myObject)

//Apresentando somente a propriedade selecionada
console.log(myObject.name)
console.log(myObject.age)
console.log(myObject.isMarried)