//O loop for é uma estrutura de repetição que permite executar um bloco de código varias vezes


//esse loop ira imprimir do 0 ao 9
for (let i = 0; i < 10; i++) {
    console.log(i);
}

//Loop para percorrer linhas e colunas [Matrizes]
for (let row = 0; row < 3; row++){
    for (let column = 0; column < 3; column++) {
        console.log(row, column);
    }
}